    private GoogleMap mMap;
    String type = "hospital";
    double latitude = 23.7104;
    double longitude = 90.40744;
    private int PROXIMITY_RADIUS = 10000;

    private View rootView;
    MapView mMapView;


    public MapFragment() {

    }

